package Clases;

public abstract class GalletaFortuna {

    private int NoGalleta;

    private String DefGalleta;

    public int getNoGalleta() {
        return NoGalleta;
    }

    public void setNoGalleta(int noGalleta) {
        NoGalleta = noGalleta;
    }

    public String getDefGalleta() {
        return DefGalleta;
    }

    public void setDefGalleta(String defGalleta) {
        DefGalleta = defGalleta;
    }
}
